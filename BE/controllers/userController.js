// API COntroller function to register an user using Email, name , password //
const registerUser = async (req, res) => {
  try {
    const { name, email, password } = req.body;
    // Null Value can't create an User account //
    if (!name || !password || !email) {
      return res.json({ success: false, message: "Missing User Details" });
    }
    // Email format validation //
    if (!validator.isEmail(email)) {
      return res.json({ success: false, message: "Enter a valid Email" });
    }
    // Pw validating atleast 8 char //
    if (password.length < 8) {
      return res.json({ success: false, message: "Choose a Strong Password" });
    }
    // Hashing of User Password //
    const salt = await bcrypt.genSalt(10);
    const hashedPw = await bcrypt.hash(password, salt);
    // Variable to store user Data for Updating on the Database//
    const userData = {
      name,
      email,
      password: hashedPw,
    };

    const newUser = new userModel(userData);
    const user = await newUser.save();
    // Token for each _id for login users //
    const token = jwt.sign({ id: user._id }, process.env.JWT_SECRET);
    res.json({ success: true, token });
  } catch (error) {
    console.log(error);
    res.json({ success: false, message: error.message });
  }
};

//API for User Login //
const loginUser = async (req, res) => {
  try {
    const { email, password } = req.body;
    const user = await userModel.findOne({ email });
    if (!user) {
      return res.json({ success: false, message: "User Doesn't Exist" });
    }
    const isMatch = await bcrypt.compare(password, user.password);
    if (isMatch) {
      const token = jwt.sign({ id: user._id }, process.env.JWT_SECRET);
      res.json({ success: true, token });
    } else {
      res.json({ success: false, message: "Invalid User Credentials" });
    }
  } catch (error) {
    console.log(error);
    res.json({ success: false, message: error.message });
  }
};

